// // Loops in java :

// there are 4 types of loops in java -

// 1. for loop
// 2. while loop
// 3. do-while loop
// 4. for-each loop

// // 1. for loop :- Used when you know how many times you want to run the loop

// Syntax : for(initialization;condition;update){
// code
// }
// Use case: Fixed iterations (like printing 1 to N, arrays traversal)

// 2. While loop: - Used when you don’t know exact iterations, but condition
// matters.

// syntax : while (condition) {
// code
// }

// key point : - Condition is checked before execution
// - If condition is false initially → loop won’t run even once

// 3. do-while loop :- Used when you want the loop to run minimum one time, even
// if condition is false.

// Syntax : do {
// code
// } while (condition);

// key point : Condition is checked after execution

// 4. for-each loop : also known as enhanced for loop

// Syntax : for (datatype variable : collection) {
// code
// }

// 🔹 Key Point
// No index required
// Cannot modify array elements directly (no index access)

// note : other concepts of loops are : Infinite loop and nested loop.

// * infinnite loop

// Syntax : while (true) {
// System.out.println("Infinite");
// }

// * Nested loop - when we use the two for-loop together - these are the nested
// for loop